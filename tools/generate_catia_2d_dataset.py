#!/usr/bin/env python3
"""
Generate CATIA-like 2D drawing patches for digit OCR training.

The images are deterministic synthetic drawings, not screenshots of CATIA.
They mimic common 2D drawing contexts: dimension lines, leaders, centerlines,
title blocks, datum grids, and light paper/scan noise. Labels are generated by
code so every sample has an exact OCR target and text bounding box.
"""

from __future__ import annotations

import argparse
import csv
import json
import math
import random
import shutil
from dataclasses import dataclass
from pathlib import Path

from PIL import Image, ImageDraw, ImageEnhance, ImageFilter, ImageFont


REPO_ROOT = Path(__file__).resolve().parents[1]
DEFAULT_FONT_DIR = REPO_ROOT / "training_data" / "fonts"
DEFAULT_OUT_DIR = REPO_ROOT / "training_data" / "catia_2d_digits"
CHARS = "0123456789.,:-/¥￥"


@dataclass(frozen=True)
class FontSpec:
    path: Path
    source: str


@dataclass(frozen=True)
class RenderedText:
    label: str
    bbox: tuple[int, int, int, int]
    font: FontSpec


def find_fonts(custom_font_paths: list[str], font_dir: Path) -> tuple[list[FontSpec], bool]:
    fonts: list[FontSpec] = []

    for raw_path in custom_font_paths:
        path = Path(raw_path).expanduser().resolve()
        if path.exists() and path.suffix.lower() in {".ttf", ".otf"}:
            fonts.append(FontSpec(path=path, source="custom"))

    if font_dir.exists():
        for path in sorted(font_dir.glob("*")):
            if path.suffix.lower() in {".ttf", ".otf"}:
                fonts.append(FontSpec(path=path.resolve(), source="training_data/fonts"))

    if fonts:
        return fonts, False

    fallback_names = [
        "C:/Windows/Fonts/arial.ttf",
        "C:/Windows/Fonts/arialbd.ttf",
        "C:/Windows/Fonts/consola.ttf",
        "C:/Windows/Fonts/consolab.ttf",
        "C:/Windows/Fonts/cour.ttf",
        "C:/Windows/Fonts/courbd.ttf",
        "C:/Windows/Fonts/segoeui.ttf",
        "C:/Windows/Fonts/bahnschrift.ttf",
    ]
    fallback = [
        FontSpec(path=Path(name), source="windows-surrogate")
        for name in fallback_names
        if Path(name).exists()
    ]
    if not fallback:
        raise SystemExit("No usable .ttf/.otf font found. Put a licensed font in training_data/fonts.")
    return fallback, True


def split_for_index(index: int, total: int) -> str:
    train_cutoff = int(total * 0.8)
    val_cutoff = int(total * 0.9)
    if index < train_cutoff:
        return "train"
    if index < val_cutoff:
        return "val"
    return "test"


def random_label(rng: random.Random) -> tuple[str, str]:
    pattern = rng.choice(
        [
            "plain_mm",
            "two_decimal",
            "one_decimal",
            "tolerance_range",
            "coordinate",
            "drawing_no",
            "date",
            "sheet",
            "mass",
            "scale",
        ]
    )

    if pattern == "plain_mm":
        return str(rng.randint(2, 999)), pattern
    if pattern == "two_decimal":
        return f"{rng.randint(1, 399)}.{rng.randint(0, 99):02d}", pattern
    if pattern == "one_decimal":
        return f"{rng.randint(1, 999)}.{rng.randint(0, 9)}", pattern
    if pattern == "tolerance_range":
        base = f"{rng.randint(3, 199)}.{rng.randint(0, 99):02d}"
        tol = f"{rng.randint(1, 9):02d}"
        return f"{base}-{tol}", pattern
    if pattern == "coordinate":
        return f"{rng.randint(0, 999)}:{rng.randint(0, 999)}", pattern
    if pattern == "drawing_no":
        return f"{rng.randint(10, 99)}-{rng.randint(1000, 9999)}", pattern
    if pattern == "date":
        return f"{rng.randint(2024, 2032):04d}/{rng.randint(1, 12):02d}/{rng.randint(1, 28):02d}", pattern
    if pattern == "sheet":
        return f"{rng.randint(1, 9)}/{rng.randint(2, 12)}", pattern
    if pattern == "mass":
        return f"{rng.randint(1, 99)},{rng.randint(0, 999):03d}", pattern
    if pattern == "scale":
        return f"{rng.choice([1, 2, 5, 10])}:{rng.choice([1, 2, 5, 10, 20, 50])}", pattern
    raise ValueError(f"unhandled label pattern: {pattern}")


def normalize_digits(value: str) -> str:
    return "".join(ch for ch in value if ch.isdigit())


def draw_arrow(draw: ImageDraw.ImageDraw, tip: tuple[float, float], angle: float, color: int, width: int) -> None:
    length = 12
    spread = math.radians(24)
    x, y = tip
    p1 = (x + length * math.cos(angle + math.pi - spread), y + length * math.sin(angle + math.pi - spread))
    p2 = (x + length * math.cos(angle + math.pi + spread), y + length * math.sin(angle + math.pi + spread))
    draw.line([tip, p1], fill=color, width=width)
    draw.line([tip, p2], fill=color, width=width)


def text_bbox(draw: ImageDraw.ImageDraw, text: str, xy: tuple[int, int], font: ImageFont.FreeTypeFont) -> tuple[int, int, int, int]:
    bbox = draw.textbbox(xy, text, font=font)
    return (int(bbox[0]), int(bbox[1]), int(bbox[2]), int(bbox[3]))


def place_text(
    draw: ImageDraw.ImageDraw,
    label: str,
    font: ImageFont.FreeTypeFont,
    xy: tuple[int, int],
    color: int,
    canvas_size: tuple[int, int],
) -> tuple[int, int, int, int]:
    width, height = canvas_size
    x, y = xy
    bbox = draw.textbbox((x, y), label, font=font)
    if bbox[2] > width:
        x -= bbox[2] - width + 4
    if bbox[3] > height:
        y -= bbox[3] - height + 4
    bbox = draw.textbbox((x, y), label, font=font)
    if bbox[0] < 0:
        x += -bbox[0] + 4
    if bbox[1] < 0:
        y += -bbox[1] + 4
    draw.text((x, y), label, font=font, fill=color)
    return text_bbox(draw, label, (x, y), font)


def draw_background(draw: ImageDraw.ImageDraw, rng: random.Random, width: int, height: int) -> None:
    line = rng.randint(155, 205)
    thin = rng.choice([1, 1, 2])

    if rng.random() < 0.45:
        for x in range(rng.randint(-40, 0), width + 40, rng.randint(42, 74)):
            draw.line([(x, 0), (x, height)], fill=rng.randint(225, 242), width=1)
        for y in range(rng.randint(-20, 0), height + 20, rng.randint(36, 68)):
            draw.line([(0, y), (width, y)], fill=rng.randint(225, 242), width=1)

    if rng.random() < 0.75:
        cx = rng.randint(width // 4, width * 3 // 4)
        cy = rng.randint(height // 4, height * 3 // 4)
        rx = rng.randint(width // 8, width // 3)
        ry = rng.randint(height // 8, height // 3)
        draw.ellipse((cx - rx, cy - ry, cx + rx, cy + ry), outline=line, width=thin)

    if rng.random() < 0.80:
        points = []
        for _ in range(rng.randint(4, 7)):
            points.append((rng.randint(8, width - 8), rng.randint(8, height - 8)))
        draw.line(points, fill=line, width=thin, joint="curve")

    if rng.random() < 0.65:
        cy = rng.randint(20, height - 20)
        cx = rng.randint(30, width - 30)
        dash = 12
        gap = 7
        x = 0
        while x < width:
            draw.line([(x, cy), (min(width, x + dash), cy)], fill=rng.randint(170, 215), width=1)
            x += dash + gap
        y = 0
        while y < height:
            draw.line([(cx, y), (cx, min(height, y + dash))], fill=rng.randint(170, 215), width=1)
            y += dash + gap


def render_horizontal_dimension(
    image: Image.Image,
    label: str,
    font: ImageFont.FreeTypeFont,
    font_spec: FontSpec,
    rng: random.Random,
) -> RenderedText:
    draw = ImageDraw.Draw(image)
    width, height = image.size
    ink = rng.randint(0, 45)
    dim = rng.randint(45, 95)
    x1 = rng.randint(24, width // 3)
    x2 = rng.randint(width * 2 // 3, width - 24)
    y = rng.randint(height // 2, height - 32)
    ext_top = max(8, y - rng.randint(38, 74))
    draw.line([(x1, ext_top), (x1, y + 10)], fill=dim, width=1)
    draw.line([(x2, ext_top + rng.randint(-4, 4)), (x2, y + 10)], fill=dim, width=1)
    draw.line([(x1, y), (x2, y)], fill=dim, width=1)
    draw_arrow(draw, (x1, y), 0.0, dim, 1)
    draw_arrow(draw, (x2, y), math.pi, dim, 1)

    text_w = draw.textlength(label, font=font)
    bbox = font.getbbox(label)
    text_h = bbox[3] - bbox[1]
    tx = int((x1 + x2 - text_w) / 2 + rng.randint(-8, 8))
    ty = int(y - text_h - rng.randint(7, 16))
    text_box = place_text(draw, label, font, (tx, ty), ink, image.size)
    return RenderedText(label=label, bbox=text_box, font=font_spec)


def render_leader_note(
    image: Image.Image,
    label: str,
    font: ImageFont.FreeTypeFont,
    font_spec: FontSpec,
    rng: random.Random,
) -> RenderedText:
    draw = ImageDraw.Draw(image)
    width, height = image.size
    ink = rng.randint(0, 45)
    dim = rng.randint(55, 105)
    target = (rng.randint(30, width // 2), rng.randint(height // 3, height - 32))
    elbow = (target[0] + rng.randint(38, 82), target[1] - rng.randint(18, 48))
    end = (min(width - 28, elbow[0] + rng.randint(60, 130)), elbow[1])
    draw.line([target, elbow, end], fill=dim, width=1)
    angle = math.atan2(target[1] - elbow[1], target[0] - elbow[0])
    draw_arrow(draw, target, angle, dim, 1)
    draw.ellipse((target[0] - 12, target[1] - 12, target[0] + 12, target[1] + 12), outline=dim, width=1)

    bbox = font.getbbox(label)
    ty = int(end[1] - (bbox[3] - bbox[1]) - rng.randint(1, 5))
    text_box = place_text(draw, label, font, (int(elbow[0] + 8), ty), ink, image.size)
    return RenderedText(label=label, bbox=text_box, font=font_spec)


def render_title_block(
    image: Image.Image,
    label: str,
    font: ImageFont.FreeTypeFont,
    font_spec: FontSpec,
    rng: random.Random,
) -> RenderedText:
    draw = ImageDraw.Draw(image)
    width, height = image.size
    ink = rng.randint(0, 45)
    line = rng.randint(70, 125)
    left = rng.randint(width // 3, width // 2)
    top = rng.randint(height // 2, height - 70)
    right = width - rng.randint(8, 18)
    bottom = height - rng.randint(8, 18)
    draw.rectangle((left, top, right, bottom), outline=line, width=1)
    row_count = rng.randint(3, 5)
    col_count = rng.randint(2, 4)
    for row in range(1, row_count):
        y = top + (bottom - top) * row // row_count
        draw.line([(left, y), (right, y)], fill=line, width=1)
    for col in range(1, col_count):
        x = left + (right - left) * col // col_count
        draw.line([(x, top), (x, bottom)], fill=line, width=1)

    cell_x1 = left + rng.randint(5, 18)
    cell_y1 = top + rng.randint(5, max(6, (bottom - top) // 2))
    text_box = place_text(draw, label, font, (cell_x1, cell_y1), ink, image.size)
    return RenderedText(label=label, bbox=text_box, font=font_spec)


def render_table_callout(
    image: Image.Image,
    label: str,
    font: ImageFont.FreeTypeFont,
    font_spec: FontSpec,
    rng: random.Random,
) -> RenderedText:
    draw = ImageDraw.Draw(image)
    width, height = image.size
    ink = rng.randint(0, 45)
    line = rng.randint(70, 125)
    left = rng.randint(10, width // 3)
    top = rng.randint(10, height // 3)
    col_w = rng.randint(55, 90)
    row_h = rng.randint(26, 38)
    cols = rng.randint(3, 5)
    rows = rng.randint(3, 5)
    right = min(width - 10, left + col_w * cols)
    bottom = min(height - 10, top + row_h * rows)
    draw.rectangle((left, top, right, bottom), outline=line, width=1)
    for x in range(left + col_w, right, col_w):
        draw.line([(x, top), (x, bottom)], fill=line, width=1)
    for y in range(top + row_h, bottom, row_h):
        draw.line([(left, y), (right, y)], fill=line, width=1)

    target_col = rng.randint(0, max(0, (right - left) // col_w - 1))
    target_row = rng.randint(0, max(0, (bottom - top) // row_h - 1))
    tx = left + target_col * col_w + rng.randint(5, 10)
    ty = top + target_row * row_h + rng.randint(2, 7)
    text_box = place_text(draw, label, font, (tx, ty), ink, image.size)
    return RenderedText(label=label, bbox=text_box, font=font_spec)


def add_scan_artifacts(image: Image.Image, rng: random.Random) -> Image.Image:
    if rng.random() < 0.55:
        image = ImageEnhance.Contrast(image).enhance(rng.uniform(0.82, 1.32))
    if rng.random() < 0.50:
        image = image.filter(ImageFilter.GaussianBlur(radius=rng.uniform(0.15, 0.55)))
    if rng.random() < 0.45:
        pixels = image.load()
        width, height = image.size
        for _ in range(width * height // rng.randint(55, 110)):
            x = rng.randrange(width)
            y = rng.randrange(height)
            delta = rng.randint(-24, 24)
            pixels[x, y] = max(0, min(255, pixels[x, y] + delta))
    if rng.random() < 0.20:
        bg = int(image.getpixel((0, 0)))
        image = image.rotate(rng.uniform(-1.1, 1.1), resample=Image.Resampling.BICUBIC, fillcolor=bg)
    return image


def render_sample(
    label: str,
    pattern: str,
    font_spec: FontSpec,
    rng: random.Random,
    canvas_size: tuple[int, int],
) -> tuple[Image.Image, RenderedText, str]:
    width, height = canvas_size
    bg = rng.randint(238, 255)
    image = Image.new("L", canvas_size, bg)
    draw = ImageDraw.Draw(image)
    draw_background(draw, rng, width, height)

    font_size = rng.randint(18, 30)
    font = ImageFont.truetype(str(font_spec.path), font_size)
    template_name = rng.choice(["horizontal_dimension", "leader_note", "title_block", "table_callout"])

    if template_name == "horizontal_dimension":
        rendered = render_horizontal_dimension(image, label, font, font_spec, rng)
    elif template_name == "leader_note":
        rendered = render_leader_note(image, label, font, font_spec, rng)
    elif template_name == "title_block":
        rendered = render_title_block(image, label, font, font_spec, rng)
    else:
        rendered = render_table_callout(image, label, font, font_spec, rng)

    image = add_scan_artifacts(image, rng)
    image.info["label_pattern"] = pattern
    return image, rendered, template_name


def write_charset(out_dir: Path) -> None:
    (out_dir / "charset.txt").write_text("\n".join(["<blank>", *CHARS]) + "\n", encoding="utf-8")


def write_preview(out_dir: Path, sample_paths: list[Path], columns: int = 4) -> None:
    if not sample_paths:
        return

    thumbs = []
    for path in sample_paths[:24]:
        image = Image.open(path).convert("L")
        image.thumbnail((240, 120))
        thumbs.append(image.copy())

    if not thumbs:
        return

    pad = 10
    cell_w = 260
    cell_h = 140
    rows = math.ceil(len(thumbs) / columns)
    preview = Image.new("L", (columns * cell_w + pad, rows * cell_h + pad), 245)
    for index, thumb in enumerate(thumbs):
        x = pad + (index % columns) * cell_w
        y = pad + (index // columns) * cell_h
        preview.paste(thumb, (x, y))
    preview.save(out_dir / "preview_grid.png")


def clean_output_dir(out_dir: Path) -> None:
    for name in ["train", "val", "test"]:
        path = out_dir / name
        if path.exists():
            shutil.rmtree(path)
    for name in ["labels.csv", "metadata.json", "charset.txt", "preview_grid.png"]:
        path = out_dir / name
        if path.exists():
            path.unlink()


def generate_dataset(args: argparse.Namespace) -> None:
    rng = random.Random(args.seed)
    fonts, is_surrogate = find_fonts(args.font_path, args.font_dir)
    args.output_dir.mkdir(parents=True, exist_ok=True)
    if args.clean:
        clean_output_dir(args.output_dir)
    write_charset(args.output_dir)

    manifest_path = args.output_dir / "labels.csv"
    rows: list[dict[str, str | int]] = []
    preview_paths: list[Path] = []

    for index in range(args.count):
        split = split_for_index(index, args.count)
        split_dir = args.output_dir / split
        split_dir.mkdir(parents=True, exist_ok=True)

        label, pattern = random_label(rng)
        font = rng.choice(fonts)
        image, rendered, template_name = render_sample(label, pattern, font, rng, (args.width, args.height))
        filename = f"{split}/{index:06d}.png"
        output_path = args.output_dir / filename
        image.save(output_path)
        if len(preview_paths) < 24:
            preview_paths.append(output_path)

        x1, y1, x2, y2 = rendered.bbox
        rows.append(
            {
                "file": filename,
                "label": label,
                "normalized_digits": normalize_digits(label),
                "template": template_name,
                "label_pattern": pattern,
                "bbox_left": max(0, x1),
                "bbox_top": max(0, y1),
                "bbox_width": max(1, x2 - x1),
                "bbox_height": max(1, y2 - y1),
                "canvas_width": args.width,
                "canvas_height": args.height,
                "font": str(font.path),
                "font_source": font.source,
                "split": split,
            }
        )

    with manifest_path.open("w", newline="", encoding="utf-8") as fp:
        writer = csv.DictWriter(
            fp,
            fieldnames=[
                "file",
                "label",
                "normalized_digits",
                "template",
                "label_pattern",
                "bbox_left",
                "bbox_top",
                "bbox_width",
                "bbox_height",
                "canvas_width",
                "canvas_height",
                "font",
                "font_source",
                "split",
            ],
        )
        writer.writeheader()
        writer.writerows(rows)

    metadata = {
        "name": "catia_2d_digits",
        "description": "Synthetic CATIA-like 2D drawing patches for digit OCR training.",
        "count": args.count,
        "width": args.width,
        "height": args.height,
        "seed": args.seed,
        "charset": CHARS,
        "surrogate_fonts": is_surrogate,
        "templates": ["horizontal_dimension", "leader_note", "title_block", "table_callout"],
        "label_patterns": [
            "plain_mm",
            "two_decimal",
            "one_decimal",
            "tolerance_range",
            "coordinate",
            "drawing_no",
            "date",
            "sheet",
            "mass",
            "scale",
        ],
        "fonts": [{"path": str(font.path), "source": font.source} for font in fonts],
        "note": (
            "These are deterministic synthetic drawings. Add licensed CATIA/CTIS-like fonts "
            "to training_data/fonts for closer target-domain typography."
        ),
    }
    (args.output_dir / "metadata.json").write_text(
        json.dumps(metadata, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    write_preview(args.output_dir, preview_paths)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate CATIA-like 2D drawing digit OCR data.")
    parser.add_argument("--font-path", action="append", default=[], help="Licensed TTF/OTF path. Can be repeated.")
    parser.add_argument("--font-dir", type=Path, default=DEFAULT_FONT_DIR)
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_OUT_DIR)
    parser.add_argument("--count", type=int, default=1200)
    parser.add_argument("--width", type=int, default=384)
    parser.add_argument("--height", type=int, default=192)
    parser.add_argument("--seed", type=int, default=20260602)
    parser.add_argument("--clean", action="store_true", help="Remove previous generated files in the output directory first.")
    return parser.parse_args()


if __name__ == "__main__":
    generate_dataset(parse_args())
